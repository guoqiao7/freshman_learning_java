/*
数组的声明
方式一:
    数据类型[] 数组名;
方式二:
    数据类型 数组名[]



 */
public class Demo {
    public static void main(String[] args) {
        // 方式一:
        //    数据类型[] 数组名;

        int[] arr1;


        //方式二:
        //    数据类型 数组名[]
        int arr2[];
        // 不要使用第二种方式
        // Variable 'arr1' might not have been initialized
        // 没有初始化 不能直接使用
        // System.out.println(arr1);

    }
}
